module Beginner
  class BasicArithmeticOperations
    def plus(a, b)
      a + b
    end

    def minus(a, b)
      a - b
    end

    def multiplication(a, b)
      a * b
    end

    def division(a, b)
      a / b
    end

    def module(a, b)
      a % b
    end

    def potency(a, b)
      a ** b
    end
  end
end